<h2>Case Studies in using WAMAP</h2>

<p>Colleges, universities, and K12 schools have found unique ways to use WAMAP.  Here's
a few examples.

<h4>Homework - Seattle Central Community College</h4>
<p>Seattle Central was interested in using online homework as a supplement to on-campus
courses, but found that all the
publisher-provided solutions added an additional cost, either to the book itself or
by requiring students with used books to buy access codes.  WAMAP provided a way for
Seattle Central's instructors to use online homework without additional cost to the
college or the students.</p>

<p>With online homework, Seattle Central's students using WAMAP get immediate feedback
on their homework, allowing them to reassess their mistakes and often self-correct.
When they have trouble, they can ask their teacher for help.  Unlike book problems
which provide no incentive for the student to revisit a question after it is
discussed in class, WAMAP homework is personalized for each student.  This way, a student
can use the approach discussed in class to work through their own version of the question.</p>

<p>Seattle Central took advantage of WAMAP's capability to easily share materials by
creating entire course sets of homework assignments and making them available to their
adjunct instructors.  This way adjuncts would use WAMAP without the time committment
needed to get things set up the first time it is used with a course.</p>

<p>Seattle Central has seen use increase as instructors see what their colleages
have been doing with WAMAP.  More instructors are starting to write and customize
their own questions.  The more experienced faculty are leading training sessions for
instructors getting started.</p>

<h4>Fully Online Courses - Pierce College</h4>
<p>Unsatisfied with BlackBoard or Angel's math assessment capabilities, but unwilling
to add the additional cost of using publisher-produced solutions, Pierce College instructors
turned to WAMAP to add enhanced math assessment to their online Statistics and
Math in Society courses.  The Math in Society course is run exclusively through WAMAP; the
statistics course uses WAMAP for assessment and Angel for other course management purposes.</p>

<p>Since WAMAP can automatically grade math expressions and numerical answers,
the online courses were able to deliver free response questions that require a deeper
level of thinking than multiple-choice questions do.  Questions being
algorithmically generated helped reduce concerns about cheating.  In both courses,
rich multipart assignments help guide the student through critical analysis of complex
problems, all while providing them feedback as they progress</p>

<h4>Placement Testing - Green River Community College</h4>
<p>Green River Community College realized that their nationally-normed, commercially
available placement test did not accurately place all of their students, so they
developed their own supplementary test to use if students disagreed with their
initial placement.  For several years, they
delivered their supplemental test paper-and-pencil, multiple choice.  While the
multiple-choice questions allowed for quick grading, they still required hand processing</p>

<p>Green River rebuilt their placement test in WAMAP, allowing them to make some
problems free-response, eliminating the ability for students to work backwards from
provided choices.  By delivering their placement in WAMAP, they also have eliminated
the need for hand grading.  With the old tests, students were limited to taking
the tests when math faculty were available to administer them.  Now they can
take the tests anytime the testing center is open.  The testing center staff can
pull up students' scores from their computer as soon as students are done testing.</p>

<h4>Diagnostic Testing - Renton School District</h4>
<p>Renton School District had about one thousand fifth graders that they wanted
to assess as they transitioned into sixth grade.  In previous years, they had delivered
a paper-and-pencil assessment, creating a grading and recording nightmare.  By moving
the assessment into WAMAP, they were able to deliver the assessment and have it
automatically graded and the scores recorded in a spreadsheet.  Individual teachers
were able to view their students' scores.</p>

<p>Since this experience has been so successful, Renton School District is hoping
to use WAMAP services as they modify their current placement test for 6th and 7th
graders, as well.  In addition, they will be making adjustments to the assessment
administered to 8th graders for placement in high school Algebra and support classes.
</p>

